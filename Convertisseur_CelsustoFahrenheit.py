#Convertisseur de Celsius (°C) en Fahrenheit (°F)

Celsius = float(input("Entrez la Température en Degrés Celsius : "))

Fahrenheit = Celsius * 9 /  5 + 32

print(Celsius, "degrés Celsius équivalent à", Fahrenheit, "degrés Fahrenheit")



